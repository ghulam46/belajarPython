# String / text
teks = "Halo"
print(teks, "Merupakan tipe data: ", type(teks))
# print(type(teks))

# Integer / angka
angka = 1
print(angka, "Merupakan tipe data: ", type(angka))

# Float / angka dengan koma
angka2 = 30.5
print(angka2, "Merupakan tipe data: ", type(angka2))

# Boolean
bool = True
print(bool, "Merupakan tipe data: ", type(bool))

# list
list = [1,2,3]
print(list, "Merupakan tipe data: ", type(list))
